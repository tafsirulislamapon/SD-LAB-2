public class Operators {
    public static void main(String[] args)
    {
        int a = 3005;
        int b = 344;

        int add = a+b;
        int subs = a-b;
        int multi = a*b;
        int div = a/b;
        int rem = a%b;

        System.out.println("Add : "+add);
        System.out.println("Subs : "+ subs);
        System.out.println("Multi : "+multi);
        System.out.println("Div :"+div);
        System.out.println("Reminder :"+rem);
    }
}
